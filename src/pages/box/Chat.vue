<script>
import Header from "../../components/layouts/Header";
import Footer from "../../components/layouts/Footer";
import Notification from "../../components/box/Notification";

import axios from "../../utils";

export default {
  data() {
    return {
      box: "",
      id: this.$route.params.id,
    };
  },
  components: {
    Header,
    Footer,
    Notification,
  },
  methods: {
    getBox() {
      axios.get(`box/${this.id}`).then((response) => {
        if (response.data.status) {
          this.box = response.data.data;
        }
      });
    },

    success(name, action) {
      this.$bvToast.toast(`${name} đã được ${action} thành công.`, {
        title: "Thông báo",
        variant: "success",
        solid: true,
      });
    },

    failure(error) {
      let err = "";
      for (const property in error.errors) {
        err += `${error.errors[property]}`;
      }

      this.$bvToast.toast(`${err}`, {
        title: `Có lỗi vui lòng thử lại`,
        variant: "danger",
        solid: true,
      });
    },
  },

  mounted() {
    this.getBox();
  },
};
</script>
